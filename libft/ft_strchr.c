#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	sym;

	sym = c;
	while (*s != '\0')
		if (*s++ == c)
			return ((char *)s - 1);
	if (c == '\0')
		return ((char *)s);
	return (NULL);
}
