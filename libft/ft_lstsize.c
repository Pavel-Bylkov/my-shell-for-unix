#include "libft.h"

int		ft_lstsize(t_list *lst)
{
	int		k;
	t_list	*tmp;

	k = 0;
	tmp = lst;
	while (tmp != NULL)
	{
		tmp = tmp->next;
		k++;
	}
	return (k);
}
